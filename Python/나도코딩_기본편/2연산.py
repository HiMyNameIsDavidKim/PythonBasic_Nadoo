#연산 공부1(사칙연산)
print(1+1)
print(3-2)
print(5*2)
print(6/3)

#연산 공부2(사칙연산 응용)
print(2**3) #제곱
print(5%3) #나누기의 나머지 구하기
print(5//3) #나누기의 몫 구하기

#연산 공부3(불린형태의 답안)
print(10 > 3) #부등호 판정
print(4 >= 7)
print(3 == 3) #같은지 판정
print(4 == 2) 
print(1 != 3) #다른지 판정
print(not(1 !=3)) #Not 함수 섞을 수 있음
print((3 > 0) and (3 > 5)) #and 판정 가능
print((3 > 0) & (3 > 5)) #and 랑 같은 의미임
print((3 > 0) or (3 > 5)) #or 가능
print((3 > 0) | (3 > 5)) #or 랑 같은 의미임
